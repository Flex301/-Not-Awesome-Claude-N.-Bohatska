def generate_spokiy():
    """
    Спокій — це не коли функція має return.
    Спокій — це коли return не потрібен.
    """
    not_part = random.choice(SPOKIY_NOT)
    yes_part = random.choice(SPOKIY_YES)
    return f"Спокій — це не {not_part}.\nСпокій — це {yes_part}."


def generate_fake_stat():
    """99.9% генераторів статистики працюють на ML. Наш — на random.choice()."""
    return random.choice(FAKE_STATS)


def generate_diagnosis():
    """
    Коли розробник боїться генерувати контент, він часто:
    • використовує шаблони
    • копіює з Stack Overflow
    • не усвідомлює що він робить
    """
    intro = random.choice(DIAGNOSIS_INTRO)
    points = random.sample(DIAGNOSIS_POINTS, k=random.randint(3, 5))
    bullets = "\n".join(f"  • {p}" for p in points)
    return f"{intro}\n{bullets}"


def generate_client_story():
    """Анонімний кейс. Порушення конфіденційності? Ні, не чув."""
    story = random.choice(CLIENT_STORIES)
    quote = random.choice(CLIENT_QUOTES)
    return f"{story}\n{quote}\n\nКлієнт. Зрозуміло."


def generate_thread_split():
    """Одна думка. 15 постів. Maximum engagement."""
    thought = generate_spokiy()
    words = thought.split()
    posts = []
    chunk_size = random.randint(1, 3)
    i = 0
    post_num = 1
    while i < len(words):
        chunk = " ".join(words[i:i + chunk_size])
        posts.append(f"  [{post_num}] {chunk}")
        i += chunk_size
        chunk_size = random.randint(1, 3)
        post_num += 1
    # Додаємо фінальний пост
    posts.append(f"  [{post_num}] {random.choice(THREAD_SPLITTERS)}")
    header = f"📱 THREAD ({post_num} постів, одна думка):\n"
    return header + "\n".join(posts)


def generate_full_post():
    """Повний мікс — як справжній акаунт."""
    sections = [
        generate_spokiy,
        generate_fake_stat,
        generate_diagnosis,
        generate_client_story,
        lambda: random.choice(NE_SLABKIST),
        lambda: random.choice(GURU_PHRASES),
        lambda: random.choice(RANDOM_PERSONAL),
    ]

    # Обираємо 3-4 рандомні секції
    chosen = random.sample(sections, k=random.randint(3, 4))
    parts = [fn() for fn in chosen]
    return "\n\n".join(parts)


